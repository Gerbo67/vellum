export const translations: Record<string, Translations> = { es, en }

/**
 * Returns the full translation object for the currently active locale.
 * Falls back to Spanish (`es`) if the stored language code is not recognized.
 */
export function useI18n(): Translations {
  const lang = useLangStore((s) => s.lang)
  return translations[lang] ?? translations.es
}
